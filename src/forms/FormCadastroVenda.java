    private void preencheVendasRealizadas() {

        VendaDAO vendaDAO = new VendaDAO();
        List<Venda> listaVenda = vendaDAO.getListaVenda();
        SimpleDateFormat formatarDate = new SimpleDateFormat("dd/MM/yyyy");
        NumberFormat moeda = NumberFormat.getCurrencyInstance(Locale.getDefault());

        DefaultTableModel tabelaVenda = (DefaultTableModel) tblPesquisaVenda.getModel();
        tabelaVenda.setNumRows(0);
        for (Venda v : listaVenda) {
            Object[] obj = new Object[]{
                v.getId_funcionario().getId_funcionario(),
                v.getId_funcionario().getNome_funcionario(),
                v.getId_venda(),
                moeda.format(v.getValor_total()),
                formatarDate.format(v.getData_venda())

            };
            tabelaVenda.addRow(obj);
        }
    }

    private void preencheCodigoFuncionarioCmb() {
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
        Funcionario funcionario = funcionarioDAO.getFuncionarioPorNome(cmbNomeFuncionario.getSelectedItem().toString());
        txtCodFuncionario.setText(String.valueOf(funcionario.getId_funcionario()));
    }

    private void preencheCodigoProdutoCmb() {
        ProdutoDAO produtoDAO = new ProdutoDAO();
        Produto produto = produtoDAO.getProdutoPorNome(cmbNomeProduto.getSelectedItem().toString());
        txtCodProduto.setText(produto.getCod_barra());
    }

    private void listarFuncionarios() {
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
        List<Funcionario> listaFuncionario = funcionarioDAO.getListaFuncionarioCargo("Operador de Caixa");
        cmbNomeFuncionario.removeAllItems();
        for (Funcionario f : listaFuncionario) {
            cmbNomeFuncionario.addItem(f.getNome_funcionario());
        }
    }

    private void preencherProduto() {
        ProdutoDAO produtoDAO = new ProdutoDAO();
        List<Produto> listaProduto = produtoDAO.getListaProduto("");
        cmbNomeProduto.removeAllItems();
        for (Produto p : listaProduto) {
            cmbNomeProduto.addItem(p.getNome_produto());
        }

    }

    private void preencheValorTotal() {
        NumberFormat moeda = NumberFormat.getCurrencyInstance(Locale.getDefault());

        double soma = 0, valor;
        int cont = tblVendas.getRowCount();
        for (int i = 0; i < cont; i++) {
            valor = (double) tblVendas.getValueAt(i, 4);
            soma += valor;
        }
        total_venda = soma;
        txtTotalVenda.setText(moeda.format(soma));

    }

    private void limparProdutoVenda() {
        txtTotalVenda.setText("");
        DefaultTableModel tabelaProduto = (DefaultTableModel) tblVendas.getModel();
        tabelaProduto.setNumRows(0);
    }


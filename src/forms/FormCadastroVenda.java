    private void btnAdicionarVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarVendaActionPerformed

        ProdutoDAO produtoDAO = new ProdutoDAO();
        Produto listaProduto = produtoDAO.getProduto(txtCodProduto.getText());
        DefaultTableModel tabelaProduto = (DefaultTableModel) tblVendas.getModel();
        int quantidade = Integer.parseInt(txtQuantidade.getValue().toString());
        int cont = 0;

        for (int i = 0; i < cont; i++) {
            tabelaProduto.setNumRows(0);
        }
        tabelaProduto.addRow(new Object[]{
            listaProduto.getCod_barra(),
            listaProduto.getNome_produto(),
            txtQuantidade.getValue().toString(),
            listaProduto.getValor_produto(),
            quantidade * listaProduto.getValor_produto(),});
        preencheValorTotal();
        txtQuantidade.setValue(1);

    }//GEN-LAST:event_btnAdicionarVendaActionPerformed


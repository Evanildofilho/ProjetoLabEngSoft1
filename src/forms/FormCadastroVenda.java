    private void btnExcluirItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirItemActionPerformed
        TelaLogin tela = new TelaLogin();
        tela.setVisible(true);

        switch (JOptionPane.showConfirmDialog(null, "Deseja excluir Produto?")) {
            case 0:
                DefaultTableModel tabelaProduto = (DefaultTableModel) tblVendas.getModel();
                int linhaSelecionada = tblVendas.getSelectedRow();
                if (linhaSelecionada == -1) {
                    JOptionPane.showMessageDialog(this, "Por favor, Selecione uma linha do produto pra excluir");
                } else {
                    tabelaProduto.removeRow(linhaSelecionada);
                    preencheValorTotal();
                    JOptionPane.showMessageDialog(this, "Produto Excluído!");
                }
                break;

            case 1:
                JOptionPane.showMessageDialog(this, "Nenhuma Exclusão feita!");
                break;
            case 2:
                break;
        }


    }//GEN-LAST:event_btnExcluirItemActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        TelaLogin tela = new TelaLogin();
        tela.setVisible(true);

        switch (JOptionPane.showConfirmDialog(null, "Deseja excluir a venda?")) {
            case 0:
                DefaultTableModel tabelaProduto = (DefaultTableModel) tblPesquisaVenda.getModel();
                int linhaSelecionada = tblPesquisaVenda.getSelectedRow();
                if (linhaSelecionada == -1) {
                    JOptionPane.showMessageDialog(this, "Por favor, Selecione uma linha da venda pra excluir");
                } else {
                    int id_venda = 0;
                    int cont = tblPesquisaVenda.getRowCount();
                    for (int i = 0; i < cont; i++) {
                        id_venda = (int) tblPesquisaVenda.getValueAt(i, 2);
                    }

                    VendaDAO vendaDAO = new VendaDAO();
                    vendaDAO.excluir(String.valueOf(id_venda));
                    preencheVendasRealizadas();
                    JOptionPane.showMessageDialog(this, "Venda Excluída!");
                }
                break;

            case 1:
                JOptionPane.showMessageDialog(this, "Nenhuma Exclusão feita!");
                break;
            case 2:
                break;
        }
    }//GEN-LAST:event_jButton5ActionPerformed


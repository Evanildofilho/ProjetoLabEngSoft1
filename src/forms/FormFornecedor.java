    private void btnCnpjPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCnpjPesquisaActionPerformed
        String cnpjPesquisa = txtCnpjPesquisa.getText();
        FornecedorDAO fornecedorDAO = new FornecedorDAO();
        Fornecedor fornecedor = fornecedorDAO.getFornecedorPorCnpj(cnpjPesquisa);
        if (fornecedor == null) {
            JOptionPane.showMessageDialog(this, "Fornecedor não encontrado!");
        } else {
            JOptionPane.showMessageDialog(this, "Fornecedor encontrado!");
            Pesquisar.setVisible(false);
            Cadastrar.setVisible(true);
            txtCodFuncionario.setText(String.valueOf(fornecedor.getFk_id_funcionario().getId_funcionario()));
            txtCnpj.setText(cnpjPesquisa);
            txtNomeFantasia.setText(fornecedor.getNomeFantasia());
            txtTelefone.setText(fornecedor.getTelefone());
            txtEmail.setText(fornecedor.getEmail());

        }
    }//GEN-LAST:event_btnCnpjPesquisaActionPerformed


    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (txtCnpj.getText().equals("##.###.###/0001-##")) {
            JOptionPane.showMessageDialog(null, "Códico de barra está vazio! seu preenchimento é Obrigatório", "Alerta!", JOptionPane.WARNING_MESSAGE);
        } else if (txtNomeFantasia.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Nome do Produto está vazio! seu preenchimento é Obrigatório", "Alerta!", JOptionPane.WARNING_MESSAGE);
        } else if (txtTelefone.getText().equals("(##) #####-####")) {
            JOptionPane.showMessageDialog(null, "Valor do produto está vazio! seu preenchimento é Obrigatório", "Alerta!", JOptionPane.WARNING_MESSAGE);
        } else if (txtEmail.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Lote está vazio! seu preenchimento é Obrigatório", "Alerta!", JOptionPane.WARNING_MESSAGE);
        } else {

            String cnpj = txtCnpj.getText();
            String nomeFantasia = txtNomeFantasia.getText();
            String telefone = txtTelefone.getText();
            String email = txtEmail.getText();

            Funcionario funcionario = new Funcionario();
            funcionario.setId_funcionario(Integer.parseInt(txtCodFuncionario.getText()));

            Fornecedor fornecedor = new Fornecedor();
            fornecedor.setFk_id_funcionario(funcionario);
            fornecedor.setCnpj(cnpj);
            fornecedor.setNomeFantasia(nomeFantasia);
            fornecedor.setTelefone(telefone);
            fornecedor.setEmail(email);

            FornecedorDAO fornecedorDAO = new FornecedorDAO();
            fornecedorDAO.inserirFornecedor(fornecedor);

            limpaCampo();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        switch (JOptionPane.showConfirmDialog(null, "Deseja editar Fornecedor?")) {
            case 0:
                String cnpj = txtCnpj.getText();
                String nomeFantasia = txtNomeFantasia.getText();
                String telefone = txtTelefone.getText();
                String email = txtEmail.getText();

                Funcionario funcionario = new Funcionario();
                funcionario.setId_funcionario(Integer.parseInt(txtCodFuncionario.getText()));

                Fornecedor fornecedor = new Fornecedor();
                fornecedor.setFk_id_funcionario(funcionario);
                fornecedor.setCnpj(cnpj);
                fornecedor.setNomeFantasia(nomeFantasia);
                fornecedor.setTelefone(telefone);
                fornecedor.setEmail(email);
                
                FornecedorDAO fornecedorDAO = new FornecedorDAO();
                fornecedorDAO.editar(fornecedor);
                limpaCampo();
                break;
            case 1:
                JOptionPane.showMessageDialog(this, "Nenhuma alteração feita!");
                break;
            case 2:
                break;
        }

    }//GEN-LAST:event_btnEditarActionPerformed


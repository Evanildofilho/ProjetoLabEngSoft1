    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if (txtCodBarra.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Códico de barra está vazio! seu preenchimento é Obrigatório", "Alerta!", JOptionPane.WARNING_MESSAGE);
        } else if (txtNomeProduto.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Nome do Produto está vazio! seu preenchimento é Obrigatório", "Alerta!", JOptionPane.WARNING_MESSAGE);
        } else if (txtValorProduto.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Valor do produto está vazio! seu preenchimento é Obrigatório", "Alerta!", JOptionPane.WARNING_MESSAGE);
        } else if (txtLote.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Lote está vazio! seu preenchimento é Obrigatório", "Alerta!", JOptionPane.WARNING_MESSAGE);
        } else if (txtValidade.equals("##/##/####")) {
            JOptionPane.showMessageDialog(null, "Data está vazio! seu preenchimento é Obrigatório", "Alerta!", JOptionPane.WARNING_MESSAGE);
        } else {

            String codBarra = txtCodBarra.getText();
            String nomeProduto = txtNomeProduto.getText();
            double valor = Double.parseDouble(txtValorProduto.getText());
            int quantidade = Integer.parseInt(txtQuantidade.getValue().toString());
            String lote = txtLote.getText();
            String data = txtValidade.getText();

            Funcionario funcionario = new Funcionario();
            funcionario.setId_funcionario(Integer.parseInt(txtCodFuncionario.getText()));

            Produto produto = new Produto();
            produto.setFk_id_funcionario(funcionario);
            produto.setCod_barra(codBarra);
            produto.setNome_produto(nomeProduto);
            produto.setValor_produto(valor);
            produto.setQuantidade_produto(quantidade);

            Validade validade = new Validade();
            validade.setfk_Cod_barra(produto);
            validade.setLote(lote);
            validade.setValidade(data);

            Fornecedor fornecedor = new Fornecedor();
            fornecedor.setCnpj(txtCnpj.getText());
            Fornece fornece = new Fornece();
            fornece.setFk_cod_barra(produto);
            fornece.setFk_cnpj(fornecedor);
            ApoioDatas pegaData = new ApoioDatas();
            try {
                fornece.setData_compra((pegaData.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())))
                );
            } catch (Exception ex) {
                Logger.getLogger(FormCadastroVenda.class.getName()).log(Level.SEVERE, null, ex);
            }
            ProdutoDAO produtoDAO = new ProdutoDAO();
            produtoDAO.inserirProduto(produto);
            ValidadeDAO validadeDAO = new ValidadeDAO();
            validadeDAO.inserirValidade(validade);
            ForneceDAO forneceDAO = new ForneceDAO();
            forneceDAO.inserirFornece(fornece);

            limpaCampo();


    }//GEN-LAST:event_btnSalvarActionPerformed

    }

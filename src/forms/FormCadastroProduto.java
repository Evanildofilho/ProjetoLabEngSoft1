    private void btnCodBarraPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCodBarraPesquisaActionPerformed
        String codPesquisa = txtCodBarraPesquisa.getText();
        ProdutoDAO produtoDAO = new ProdutoDAO();
        Produto produto = produtoDAO.getProduto(codPesquisa);
        ValidadeDAO validadeDAO = new ValidadeDAO();
        Validade validade = validadeDAO.getValidade(codPesquisa);

        if (produto == null) {
            JOptionPane.showMessageDialog(this, "Produto não encontrado!");
            limpaCampo();
        } else {
            JOptionPane.showMessageDialog(this, "Produto encontrado!");
            txtCodBarra.setText(codPesquisa);
            txtNomeProduto.setText(produto.getNome_produto());
            txtValorProduto.setText(String.valueOf(produto.getValor_produto()));
            txtQuantidade.setValue(produto.getQuantidade_produto());
            txtLote.setText(validade.getLote());
            txtValidade.setText(validade.getValidade());
        }
    }//GEN-LAST:event_btnCodBarraPesquisaActionPerformed


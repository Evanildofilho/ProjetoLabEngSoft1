    private void listarFuncionarios() {
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
        List<Funcionario> listaFuncionario = funcionarioDAO.getListaFuncionarioCargo("Estoquista");
        cmbNomeFuncionario.removeAllItems();
        for (Funcionario f : listaFuncionario) {
            cmbNomeFuncionario.addItem(f.getNome_funcionario());
        }
    }

    private void listarFornecedor() {
        FornecedorDAO fornecedorDAO = new FornecedorDAO();
        List<Fornecedor> listaFornecedor = fornecedorDAO.ListaDeNomeFornecedor();
        cmbNomeFornecedor.removeAllItems();
        for (Fornecedor f : listaFornecedor) {
            cmbNomeFornecedor.addItem(f.getNomeFantasia());
        }
    }

    private void preencheCodigoFornecedorCmb() {
        FornecedorDAO fornecedorDAO = new FornecedorDAO();
        Fornecedor fornecedor = fornecedorDAO.getFornecedorPorNome(cmbNomeFornecedor.getSelectedItem().toString());
        txtCnpj.setText(fornecedor.getCnpj());
    }

    private void preencheCodigoFuncionarioCmb() {
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
        Funcionario funcionario = funcionarioDAO.getFuncionarioPorNome(cmbNomeFuncionario.getSelectedItem().toString());
        txtCodFuncionario.setText(String.valueOf(funcionario.getId_funcionario()));
    }

